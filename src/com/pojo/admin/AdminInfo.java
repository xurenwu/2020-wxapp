package com.pojo.admin;

public class AdminInfo {
	
	private int id;
	private String admin_name;
	private String admin_password;
	private String admin_desc;
	private String admin_nickname;
	private String admin_email;
	private String admin_avatar;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getAdmin_password() {
		return admin_password;
	}
	public void setAdmin_password(String admin_password) {
		this.admin_password = admin_password;
	}
	public String getAdmin_desc() {
		return admin_desc;
	}
	public void setAdmin_desc(String admin_desc) {
		this.admin_desc = admin_desc;
	}
	public String getAdmin_nickname() {
		return admin_nickname;
	}
	public void setAdmin_nickname(String admin_nickname) {
		this.admin_nickname = admin_nickname;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
	public String getAdmin_avatar() {
		return admin_avatar;
	}
	public void setAdmin_avatar(String admin_avatar) {
		this.admin_avatar = admin_avatar;
	}
	
	
	

}

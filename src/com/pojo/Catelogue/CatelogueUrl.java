package com.pojo.Catelogue;

public class CatelogueUrl {
	private int bookId;
	private String allChapterNameUrl;
	private String allChapterReadUrl;
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public String getAllChapterNameUrl() {
		return allChapterNameUrl;
	}
	public void setAllChapterNameUrl(String allChapterNameUrl) {
		this.allChapterNameUrl = allChapterNameUrl;
	}
	public String getAllChapterReadUrl() {
		return allChapterReadUrl;
	}
	public void setAllChapterReadUrl(String allChapterReadUrl) {
		this.allChapterReadUrl = allChapterReadUrl;
	}
	
	
}
